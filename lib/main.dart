class _MyHomePageState extends State<MyHomePage> {
  List<dynamic> widgets = [];

  @override
  void initState() {
    super.initState();
    loadData();
  }
  showLoadingDialog() {
    if (widgets.length == 0) {
      return true;
    }

    return false;
  }

  getBody() {
    if (showLoadingDialog()) {
      return getProgressDialog();
    } else {
      return getListView();
    }
  }

  getProgressDialog() {
    return Center(child: CircularProgressIndicator());
  }

  ListView getListView() => ListView.builder(
      itemCount: widgets.length,
      itemBuilder: (BuildContext context, int position) {
        return getRow(position);
      });

  Widget getRow(int i) {
    return Padding(padding: EdgeInsets.all(10.0), child: Text("Row ${i} ${widgets[i]['articleName']}"));
  }
  loadData() async {
    final queryParameters = {
      'category': 'TEST2',
    };
    var dataURL = Uri.http("10.0.2.2:8080","/item",queryParameters);
    http.Response response = await http.get(dataURL);
    log(response.body);
    setState(() {
      var jsonDecode1 = jsonDecode(response.body);
      widgets = jsonDecode1['content'];
    });
  }
  @override
  Widget build(BuildContext context) {
    return Scaffold(
        appBar: AppBar(
          title: Text("Sample App"),
        ),
        body: getBody());
  }
}
